void fb_clear(void) {
    uint32_t bytes_per_pixel;
    uint32_t clear_height;
    uint32_t chunk_size;
    uint32_t y_start;
    uint32_t y_end;
    uint32_t row_idx;
    uint32_t col;
    
    bytes_per_pixel = (uint32_t)(fb.bpp / 8u);
    if (bytes_per_pixel == 0) return;

    clear_height = (fb.height < hw_height) ? fb.height : hw_height;
    if (clear_height == 0) return;

    chunk_size = 8;
    for (y_start = 0; y_start < clear_height; y_start += chunk_size) {
        y_end = y_start + chunk_size;
        if (y_end > clear_height) y_end = clear_height;
        fb_clear_region(y_start, y_end);
    }
    
    fb.cursor_x = 0;
    fb.cursor_y = 0;
    
    memset(&screen_buffer[0][0], ' ', MAX_ROWS * MAX_COLS);
    
    cursor_drawn = 0;
    cursor_prev_x = 0;
    cursor_prev_y = 0;
}

void fb_putpixel(uint32_t x, uint32_t y, uint32_t color) {
    uint32_t bytes_per_pixel;
    uint32_t offset;
    uint32_t fb_size;
    uint8_t *p;
    uint8_t r;
    uint8_t g;
    uint8_t b;
    uint16_t rgb565;

    if (x >= hw_width || y >= hw_height) {
        return;
    }
    bytes_per_pixel = (uint32_t)(fb.bpp / 8u);
    if (bytes_per_pixel == 0) {
        return;
    }
    offset = y * hw_pitch + x * bytes_per_pixel;
    fb_size = hw_pitch * hw_height;
    if (offset >= fb_size || fb_size - offset < bytes_per_pixel) {
        return;
    }

    p = (uint8_t *)fb.addr + offset;
    if (fb.bpp == 32) {
        *(uint32_t *)p = color;
    } else if (fb.bpp == 24) {
        p[0] = (uint8_t)(color & 0xFF);
        p[1] = (uint8_t)((color >> 8) & 0xFF);
        p[2] = (uint8_t)((color >> 16) & 0xFF);
    } else if (fb.bpp == 16) {
        r = (uint8_t)((color >> 16) & 0xFF);
        g = (uint8_t)((color >> 8) & 0xFF);
        b = (uint8_t)(color & 0xFF);
        rgb565 = (uint16_t)(((r >> 3) << 11) | ((g >> 2) << 5) | (b >> 3));
        *(uint16_t *)p = rgb565;
    }
}

void fb_putchar(char c, uint32_t cx, uint32_t cy) {
    uint8_t uc;
    const uint8_t *glyph;
    uint32_t px;
    uint32_t py;
    uint32_t bytes_per_pixel;
    uint32_t bytes_per_line;
    uint8_t *base;
    uint32_t fg;
    uint32_t bg;
    uint32_t row;
    uint8_t bits;
    uint32_t *p32;
    uint32_t col;
    uint32_t byte_idx;
    uint8_t bit;
    uint32_t color;
    uint8_t *p;
    uint8_t r;
    uint8_t g;
    uint8_t b;
    uint16_t rgb565;

    if (!fb.font || !fb.font->glyphs) {
        return;
    }

    if (cx >= fb.cols || cy >= fb.rows) {
        return;
    }
    
    if (cy < MAX_ROWS && cx < MAX_COLS) {
        screen_buffer[cy][cx] = c;
    }
    
    uc = (uint8_t)c;
    glyph = psf_get_glyph(fb.font, uc);
    if (!glyph) {
        return;
    }
    
    px = cx * fb.font->width;
    py = cy * fb.font->height;

    bytes_per_pixel = (uint32_t)(fb.bpp / 8u);
    if (bytes_per_pixel == 0) return;

    bytes_per_line = fb.font->bytesperglyph / fb.font->height;
    if (bytes_per_line == 0) bytes_per_line = (fb.font->width + 7) / 8;

    if (fb.bpp == 32 && fb.font->width == 8 && bytes_per_line == 1) {
        base = (uint8_t *)fb.addr;
        fg = fb.fg_color;
        bg = fb.bg_color;
        for (row = 0; row < fb.font->height; row++) {
            bits = glyph[row];
            p32 = (uint32_t *)(base + (py + row) * hw_pitch + px * 4u);
            p32[0] = (bits & 0x80) ? fg : bg;
            p32[1] = (bits & 0x40) ? fg : bg;
            p32[2] = (bits & 0x20) ? fg : bg;
            p32[3] = (bits & 0x10) ? fg : bg;
            p32[4] = (bits & 0x08) ? fg : bg;
            p32[5] = (bits & 0x04) ? fg : bg;
            p32[6] = (bits & 0x02) ? fg : bg;
            p32[7] = (bits & 0x01) ? fg : bg;
        }
        return;
    }

    base = (uint8_t *)fb.addr;
    for (row = 0; row < fb.font->height; row++) {
        p = base + (py + row) * hw_pitch + px * bytes_per_pixel;
        for (col = 0; col < fb.font->width; col++) {
            byte_idx = row * bytes_per_line + col / 8;
            bit = 7 - (col % 8);
            color = (glyph[byte_idx] & (1u << bit)) ? fb.fg_color : fb.bg_color;
            if (fb.bpp == 32) {
                *((uint32_t *)(p + col * bytes_per_pixel)) = color;
            } else if (fb.bpp == 24) {
                uint8_t *dst = p + col * bytes_per_pixel;
                dst[0] = (uint8_t)(color & 0xFF);
                dst[1] = (uint8_t)((color >> 8) & 0xFF);
                dst[2] = (uint8_t)((color >> 16) & 0xFF);
            } else if (fb.bpp == 16) {
                r = (uint8_t)((color >> 16) & 0xFF);
                g = (uint8_t)((color >> 8) & 0xFF);
                b = (uint8_t)(color & 0xFF);
                rgb565 = (uint16_t)(((r >> 3) << 11) | ((g >> 2) << 5) | (b >> 3));
                *((uint16_t *)(p + col * bytes_per_pixel)) = rgb565;
            }
        }
    }
}

void fb_scroll(void) {
    uint32_t line_height;
    uint32_t copy_height;
    uint32_t bytes_per_pixel;
    uint32_t line_bytes;
    uint8_t *dst;
    uint8_t *src;
    uint32_t y;
    uint32_t last_row_start;
    uint32_t x;
    uint8_t *row;
    uint8_t *p;
    uint32_t used;
    uint32_t row_idx;
    uint32_t col;
    uint8_t r;
    uint8_t g;
    uint8_t b;
    uint16_t rgb565;
    char old_char;
    uint32_t clear_end;

    if (fb.rows == 0 || fb.cols == 0) {
        return;
    }
    if (cursor_drawn) {
        old_char = ' ';
        if (cursor_prev_y < MAX_ROWS && cursor_prev_x < MAX_COLS) {
            old_char = screen_buffer[cursor_prev_y][cursor_prev_x];
        }
        fb_putchar(old_char, cursor_prev_x, cursor_prev_y);
        cursor_drawn = 0;
    }
    
    line_height = fb.font->height;
    copy_height = (fb.rows - 1) * line_height;

    bytes_per_pixel = (uint32_t)(fb.bpp / 8u);
    if (bytes_per_pixel == 0) return;
    line_bytes = hw_pitch;
    
    dst = (uint8_t *)fb.addr;
    src = (uint8_t *)fb.addr + line_height * hw_pitch;
    
    for (y = 0; y < copy_height; y++) {
        memcpy(dst, src, line_bytes);
        dst += hw_pitch;
        src += hw_pitch;
    }
    
    last_row_start = (fb.rows - 1) * line_height;
    clear_end = last_row_start + line_height;
    if (clear_end > hw_height) clear_end = hw_height;
    if (last_row_start >= hw_height) return;
    
    for (y = last_row_start; y < clear_end; y++) {
        row = (uint8_t *)fb.addr + y * hw_pitch;
        if (fb.bg_color == 0) {
            memset(row, 0, hw_pitch);
        } else if (fb.bpp == 32) {
            uint32_t *row32 = (uint32_t *)row;
            uint32_t pixels_to_clear = hw_width;
            if (pixels_to_clear > (hw_pitch / 4)) {
                pixels_to_clear = hw_pitch / 4;
            }
            for (x = 0; x < pixels_to_clear; x++) {
                row32[x] = fb.bg_color;
            }
        } else {
            for (x = 0; x < hw_width; x++) {
                p = row + x * bytes_per_pixel;
                if (fb.bpp == 24) {
                    p[0] = (uint8_t)(fb.bg_color & 0xFF);
                    p[1] = (uint8_t)((fb.bg_color >> 8) & 0xFF);
                    p[2] = (uint8_t)((fb.bg_color >> 16) & 0xFF);
                } else if (fb.bpp == 16) {
                    r = (uint8_t)((fb.bg_color >> 16) & 0xFF);
                    g = (uint8_t)((fb.bg_color >> 8) & 0xFF);
                    b = (uint8_t)(fb.bg_color & 0xFF);
                    rgb565 = (uint16_t)(((r >> 3) << 11) | ((g >> 2) << 5) | (b >> 3));
                    *(uint16_t *)p = rgb565;
                }
            }
            used = hw_width * bytes_per_pixel;
            if (used < hw_pitch) {
                memset(row + used, 0, hw_pitch - used);
            }
        }
    }
    
    uint32_t scroll_rows = (fb.rows > 1 && fb.rows <= MAX_ROWS) ? fb.rows - 1 : 0;
    uint32_t scroll_cols = (fb.cols <= MAX_COLS) ? fb.cols : MAX_COLS;
    
    if (scroll_rows > 0 && scroll_cols > 0 && scroll_rows < MAX_ROWS) {
        memmove(&screen_buffer[0][0], &screen_buffer[1][0], 
                scroll_rows * MAX_COLS * sizeof(char));
        
        if (fb.rows > 0 && fb.rows - 1 < MAX_ROWS) {
            for (col = 0; col < scroll_cols; col++) {
                screen_buffer[fb.rows - 1][col] = ' ';
            }
        }
    }
    
    if (cursor_prev_y > 0) {
        cursor_prev_y--;
    }
}

void fb_write_char(char c) {
    uint32_t tab_stop;
    uint32_t i;

    if (fb.rows == 0 || fb.cols == 0) {
        return;
    }
    if (c == '\n') {
        fb.cursor_x = 0;
        fb.cursor_y++;
        if (fb.cursor_y >= fb.rows) {
            fb_scroll();
            fb.cursor_y = fb.rows - 1;
        }
        return;
    }
    
    if (c == '\r') {
        fb.cursor_x = 0;
        return;
    }
    
    if (c == '\b') {
        if (fb.cursor_x > 0) {
            fb.cursor_x--;
            fb_putchar(' ', fb.cursor_x, fb.cursor_y);
        }
        return;
    }
    
    if (c == '\t') {
        tab_stop = 8 - (fb.cursor_x % 8);
        for (i = 0; i < tab_stop; i++) {
            fb_write_char(' ');
        }
        return;
    }
    
    fb_putchar(c, fb.cursor_x, fb.cursor_y);
    fb.cursor_x++;
    
    if (fb.cursor_x >= fb.cols) {
        fb.cursor_x = 0;
        fb.cursor_y++;
        if (fb.cursor_y >= fb.rows) {
            fb_scroll();
            fb.cursor_y = fb.rows - 1;
        }
    }
}

void fb_write_string(const char *str) {
    while (*str) {
        fb_write_char(*str++);
    }
}

void fb_update_cursor(void) {
    char old_char;

    if (cursor_drawn && (cursor_prev_x != fb.cursor_x || cursor_prev_y != fb.cursor_y)) {
        if (cursor_prev_x < fb.cols && cursor_prev_y < fb.rows) {
            old_char = ' ';
            if (cursor_prev_y < MAX_ROWS && cursor_prev_x < MAX_COLS) {
                old_char = screen_buffer[cursor_prev_y][cursor_prev_x];
            }
            fb_putchar(old_char, cursor_prev_x, cursor_prev_y);
        }
        cursor_drawn = 0;
    }
    if (cursor_drawn && !cursor_visible) {
        if (cursor_prev_x < fb.cols && cursor_prev_y < fb.rows) {
            old_char = ' ';
            if (cursor_prev_y < MAX_ROWS && cursor_prev_x < MAX_COLS) {
                old_char = screen_buffer[cursor_prev_y][cursor_prev_x];
            }
            fb_putchar(old_char, cursor_prev_x, cursor_prev_y);
        }
        cursor_drawn = 0;
    }
    if (cursor_visible && fb.cursor_x < fb.cols && fb.cursor_y < fb.rows) {
        fb_draw_cursor_block(fb.cursor_x, fb.cursor_y, fb.fg_color);
        cursor_prev_x = fb.cursor_x;
        cursor_prev_y = fb.cursor_y;
        cursor_drawn = 1;
    }
}

void fb_tick(void) {
    static int tick_counter = 0;
    tick_counter++;
    if (tick_counter >= 500) {
        tick_counter = 0;
        cursor_visible = !cursor_visible;
        fb_update_cursor();
    }
}

