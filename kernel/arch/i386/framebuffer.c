int fb_set_mode(uint32_t width, uint32_t height, uint32_t refresh_rate) {
    static char saved_buffer[MAX_ROWS][MAX_COLS];
    uint32_t font_width;
    uint32_t font_height;
    uint32_t req_width;
    uint32_t req_height;
    int hw_changed;
    uint32_t new_pitch;
    uint8_t new_bpp;
    uint16_t try_bpps[3];
    int i;
    uint16_t bpp_try;
    uint32_t candidate_h;
    int attempts_h;
    uint32_t candidate_w;
    int attempts_w;
    uint32_t next_w;
    uint32_t next_h;
    uint32_t row;
    uint32_t col;
    uint32_t fb_size;
    uint32_t bytes_per_pixel;
    uint32_t fb_phys;
    uint32_t num_pages;
    uint32_t page_idx;
    uint32_t phys;
    uint32_t virt;
    uint32_t old_cols;
    uint32_t old_rows;
    uint32_t line_pixels;
    uint32_t effective_width;
    uint32_t max_row;
    uint32_t max_col;
    uint32_t row_rest;
    uint32_t col_rest;
    char c_rest;
    uint32_t old_mapped_pages;
    uint32_t batch_size;
    uint32_t batch_start;
    uint32_t batch_end;

    if (original_fb_width == 0 || original_fb_height == 0) {
        return -3;
    }

    if (width == 0 || height == 0) {
        return -2;
    }

    font_width = (fb.font && fb.font->width) ? fb.font->width : 8;
    font_height = (fb.font && fb.font->height) ? fb.font->height : 16;
    if (font_width == 0) font_width = 8;
    if (font_height == 0) font_height = 16;

    req_width = width;
    req_height = height;

    hw_changed = 0;
    new_pitch = hw_pitch;
    new_bpp = fb.bpp;

    if (bga_is_available()) {
        try_bpps[0] = 32;
        try_bpps[1] = 24;
        try_bpps[2] = 16;
        for (i = 0; i < 3 && !hw_changed; i++) {
            bpp_try = try_bpps[i];
            candidate_h = req_height;
            attempts_h = 0;
            while (candidate_h > 0 && attempts_h < 64 && !hw_changed) {
                candidate_w = req_width;
                attempts_w = 0;
                while (candidate_w > 0 && attempts_w < 64 && !hw_changed) {
                    if (bga_set_mode((uint16_t)candidate_w, (uint16_t)candidate_h, bpp_try, &new_pitch) == 0) {
                        hw_changed = 1;
                        new_bpp = (uint8_t)bpp_try;
                        width = candidate_w;
                        height = candidate_h;
                        break;
                    }
                    next_w = decrease_width_step(candidate_w, font_width);
                    if (next_w == 0 || next_w == candidate_w) break;
                    candidate_w = next_w;
                    attempts_w++;
                }
                if (hw_changed) break;
                next_h = decrease_step(candidate_h, font_height);
                if (next_h == 0 || next_h == candidate_h) break;
                candidate_h = next_h;
                attempts_h++;
            }
        }
    }

    if (!hw_changed) {
        if (width > hw_width) width = hw_width;
        if (height > hw_height) height = hw_height;
        if (font_width) {
            width = (width / font_width) * font_width;
        }
        if (font_height) {
            height = (height / font_height) * font_height;
        }
        if (width == 0 || height == 0) {
            return -2;
        }
    }

    for (row = 0; row < MAX_ROWS; row++) {
        for (col = 0; col < MAX_COLS; col++) {
            saved_buffer[row][col] = screen_buffer[row][col];
        }
    }

    if (hw_changed) {
        fb_size = new_pitch * height;
        if (fb_vram_bytes && fb_size > fb_vram_bytes) {
            return -5;
        }

        bytes_per_pixel = (uint32_t)(new_bpp / 8u);
        if (bytes_per_pixel == 0) {
            return -6;
        }

        old_mapped_pages = mapped_pages_count;
        
        hw_pitch = new_pitch;
        hw_height = height;
        hw_width = new_pitch / bytes_per_pixel;

        fb.pitch = new_pitch;
        fb.bpp = new_bpp;
        original_fb_width = hw_width;
        original_fb_height = height;
        original_fb_pitch = new_pitch;
        original_fb_size = new_pitch * height;

        fb_phys = (uint32_t)fb.phys_addr;
        num_pages = (fb_size + 0xFFF) / 0x1000;

        if (num_pages > old_mapped_pages) {
            batch_size = 64;

            for (batch_start = old_mapped_pages; batch_start < num_pages; batch_start += batch_size) {
                batch_end = batch_start + batch_size;
                if (batch_end > num_pages) batch_end = num_pages;

                for (page_idx = batch_start; page_idx < batch_end; page_idx++) {
                    phys = fb_phys + (page_idx * 0x1000);
                    virt = 0xE0000000 + (page_idx * 0x1000);
                    vmm_map_page(virt, phys, 0x003);
                    tlb_flush_page(virt);
                }

                memory_barrier();
                asm volatile("pause; pause; pause; pause; pause; pause; pause; pause");
            }
        } else {
            for (page_idx = 0; page_idx < num_pages; page_idx++) {
                phys = fb_phys + (page_idx * 0x1000);
                virt = 0xE0000000 + (page_idx * 0x1000);
                vmm_map_page(virt, phys, 0x003);
                if ((page_idx & 0x1F) == 0x1F) {
                    tlb_flush_page(virt);
                    asm volatile("pause; pause");
                }
            }
        }

        mapped_pages_count = num_pages;

        tlb_flush_all();
        memory_barrier();

        asm volatile("pause; pause; pause; pause; pause; pause; pause; pause");
    }

    old_cols = fb.cols;
    old_rows = fb.rows;

    if (refresh_rate > 0) {
        fb.refresh_rate = refresh_rate;
    }

    bytes_per_pixel = (uint32_t)(fb.bpp / 8u);
    if (bytes_per_pixel == 0 || hw_pitch == 0) {
        return -6;
    }

    line_pixels = hw_pitch / bytes_per_pixel;
    if (line_pixels == 0) {
        return -6;
    }

    if (fb.font && fb.font->height) {
        height = (height / fb.font->height) * fb.font->height;
        if (height == 0) {
            return -2;
        }
    }

    effective_width = width;
    if (effective_width > line_pixels) {
        effective_width = line_pixels;
    }
    if (fb.font && fb.font->width) {
        effective_width = (effective_width / fb.font->width) * fb.font->width;
        if (effective_width == 0) {
            return -2;
        }
    }

    fb.width = effective_width;
    fb.height = height;
    if (fb.font && fb.font->width && fb.font->height) {
        fb.cols = effective_width / fb.font->width;
        fb.rows = height / fb.font->height;
        if (fb.cols == 0 || fb.rows == 0) {
            return -2;
        }
        if (fb.cols > MAX_COLS) fb.cols = MAX_COLS;
        if (fb.rows > MAX_ROWS) fb.rows = MAX_ROWS;
        fb.width = fb.cols * fb.font->width;
    }

    if (fb.cols == 0 || fb.rows == 0) {
        return -2;
    }

    console_clamp_cursors(fb.cols, fb.rows);

    if (fb.cursor_x >= fb.cols) fb.cursor_x = fb.cols - 1;
    if (fb.cursor_y >= fb.rows) fb.cursor_y = fb.rows - 1;

    if (console_is_initialized()) {
        console_redraw_current();
        return 0;
    }

    fb_clear();

    max_row = (old_rows < fb.rows) ? old_rows : fb.rows;
    max_col = (old_cols < fb.cols) ? old_cols : fb.cols;

    if (max_row > MAX_ROWS) max_row = MAX_ROWS;
    if (max_col > MAX_COLS) max_col = MAX_COLS;
    if (max_row > 32) max_row = 32;
    if (max_col > 128) max_col = 128;

    for (row_rest = 0; row_rest < max_row; row_rest++) {
        for (col_rest = 0; col_rest < max_col; col_rest++) {
            c_rest = saved_buffer[row_rest][col_rest];
            screen_buffer[row_rest][col_rest] = c_rest;
            if (c_rest != ' ') {
                fb_putchar(c_rest, col_rest, row_rest);
            }
        }
        if ((row_rest & 0x01) == 0x01) {
            asm volatile("pause; pause");
        }
    }

    fb.cursor_x = 0;
    fb.cursor_y = 0;
    cursor_prev_x = 0;
    cursor_prev_y = 0;
    cursor_drawn = 0;

    return 0;
}

int fb_get_info(uint32_t *width, uint32_t *height, uint32_t *bpp, uint32_t *refresh_rate) {
    if (!width || !height || !bpp) {
        return -1; 
    }
    
    *width = fb.width;
    *height = fb.height;
    *bpp = fb.bpp;
    
    if (refresh_rate) {
        *refresh_rate = fb.refresh_rate;
    }
    
    return 0; 
}

static inline void map_physical_page(uint32_t phys) {
    uint32_t page;
    if (phys == 0) {
        return;
    }
    page = phys & ~0xFFFu;
    vmm_map_page(page + 0xC0000000u, page, 0x003);
}


int fb_get_caps(uint32_t *out_words, uint32_t words) {
    uint32_t font_w;
    uint32_t font_h;
    uint32_t flags;

    if (!out_words || words < 16) {
        return -1;
    }

    font_w = (fb.font && fb.font->width) ? fb.font->width : 8;
    font_h = (fb.font && fb.font->height) ? fb.font->height : 16;
    if (font_w == 0) font_w = 8;
    if (font_h == 0) font_h = 16;

    flags = 0;
    if (bga_is_available()) {
        flags |= 1u;
        flags |= 2u;
    }

    out_words[0] = fb.width;
    out_words[1] = fb.height;
    out_words[2] = (uint32_t)fb.bpp;
    out_words[3] = fb.pitch;
    out_words[4] = fb.cols;
    out_words[5] = fb.rows;
    out_words[6] = font_w;
    out_words[7] = font_h;
    out_words[8] = hw_width;
    out_words[9] = hw_height;
    out_words[10] = hw_pitch;
    out_words[11] = fb_vram_bytes;
    out_words[12] = flags;
    out_words[13] = fb.refresh_rate;
    out_words[14] = 0;
    out_words[15] = 0;

    return 0;
}