static uint32_t decrease_width_step(uint32_t value, uint32_t step) {
    uint32_t delta;
    if (step == 0 || value <= step) {
        return 0;
    }
    delta = value % step;
    if (delta == 0) {
        delta = step;
    }
    if (delta > value) {
        return 0;
    }
    return value - delta;
}

